let dia = "sábado";

switch (dia){
    case "domingo":
        console.log("1")
        break;
    case "segunda":
        console.log("2")
        break;
    case "terça":
        console.log("3")
        break;
    case "quarta":
        console.log("4")
        break;
    case "quinta":
        console.log("5")
        break;
    case "sexta":
        console.log("6")
        break;
    case "sábado":
        console.log("7")
        break;
    default:
        console.log("Coloque um dia da semana")
}